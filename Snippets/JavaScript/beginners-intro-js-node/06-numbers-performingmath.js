let num = 100;

// Basic Math
console.log(num + 25);  // 125
console.log(num - 100); // 0
console.log(num * 100); // 10000
console.log(num / 1500);    // 0.06666666666666667

// Additional Mathematical Operations
console.log(num % 1500); // 100, Remainder
console.log(++num); // 101, Increment
console.log(--num); // 100, Decrement

// Using the Math Object
console.log(Math.PI); // 3.141592653589793, Pi 
console.log(Math.sqrt(num)); // 10, Square root